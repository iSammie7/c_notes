#include <stdio.h>

int main() {

    int age = 17;
    int year = 2077;
    int quantity = 67;

    printf("I am %d years old! I'm a minor!\n", age);
    printf("The year is %d!\n", year);
    printf("I have %d mangoes!", quantity);

    return 0;
}

// The % in C a format specifier, it helps you to display a variable.
// Here %d means decimal, used with an integer.